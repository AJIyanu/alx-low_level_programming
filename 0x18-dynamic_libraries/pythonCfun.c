#include "cal.h"

/**
 * add - adds
 * @a: num1
 * @b: num2
 *
 * Return: a + b
 */


int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts
 * @a: num1
 * @b: num2
 *
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies
 * @a: num1
 * @b: num2
 *
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides
 * @a: num1
 * @b: num2
 *
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - last one which is modulus, gets the remainder
 * @a: num1
 * @b: num2
 *
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
